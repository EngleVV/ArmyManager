public class WordsResult {
    String words;

    public String getWords() {
        return words;
    }

    public void setWords(String words) {
        this.words = words;
    }

    @Override
    public String toString() {
        return "WordsResult{" +
                "words='" + words + '\'' +
                '}';
    }
}
